    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        try {
            $request->validate([
                'name' => 'required|string|max:255',
                'email' => 'required|string|email|max:255|unique:users',
                'password' => 'required|string|min:8',
            ]);
            $usuario = User::create([
                'name' => $request->name,
                'email' => $request->email,
                'password' => Hash::make($request->password)
            ]);
            return response()->json(['mensaje' => 'Usuario creado con Ã©xito', 'usuario' => $usuario], 201);
        } catch (\Throwable $th) {
            return response()->json(['mensaje' => $th], 500);
        }
    }

    /**
     * Display the specified resource.
     */
    public function login(Request $request)
    {
        try {
            $request->validate([
                'email' => 'required|string|email',
                'password' => 'required|string',
            ]);

            $credentials = $request->only('email', 'password');
            $user = User::where('email', $request->email)->firstOrFail();
            if (auth()->attempt($credentials)) {
                return response()->json(['mensaje' => 'Successfully logged', 'usuario' => $user], 201);
            } else {
                return response()->json(['message' => 'Unauthorized'], 401);
            }
        } catch (\Throwable $th) {
            return response()->json(['mensaje' => $th->getMessage()], 500);
        }
    }

